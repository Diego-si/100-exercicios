package exercicios;

/**
 * Programa para imprimir os numeros de 1 a 10 em ordem decrescente
 *
 * @author Diego Almeida da Silva
 * @since 10 de fev. de 2021
 */
public class Exercicio67 {

	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {
		
		//exibindo os valores em ordem decrescente
		for (int i = 10; i > 0; i--) {
			System.out.println("Números em ordem decrescente: " + i);
		}
	}

}
