package exercicios;

/**
 * Programa para imprimir os numeros de 1 a 10 em ordem crescente
 *
 * @author Diego Almeida da Silva
 * @since 10 de fev. de 2021
 */
public class Exercicio66 {

	/*
	 * Método principal para executar a classe
	 */
	public static void main(String[] args) {

		// exibindo os valores de 1 a 10 em ordem crescente
		for (int i = 1; i < 11; i++) {
			System.out.println("Números em ordem crescente:" + i);
		}
	}

}
